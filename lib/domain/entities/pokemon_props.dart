class PokemonGender {
  final bool genderless;
  final int male;
  final int female;

  PokemonGender({
    this.genderless,
    this.male,
    this.female,
  });
}

class PokemonStats {
  final int attack;
  final int specialAttack;
  final int defense;
  final int specialDefense;
  final int hp;
  final int speed;

  PokemonStats({
    this.attack,
    this.specialAttack,
    this.defense,
    this.specialDefense,
    this.hp,
    this.speed,
  });
}
